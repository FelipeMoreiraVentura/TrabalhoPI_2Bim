function getGamesMenu()
{
    global $games;

    $gamesMenu = [];

    foreach ($games as $game) {
        $gamesMenu[] = [
            "id" => $game["id"],
            "name" => $game["name"],
            "img" => $game["img"]
        ];
    }

    return $gamesMenu;
}

function getGame($id)
{
    global $games;

    $selectGame = null;

    foreach ($games as $game) {
        if ((string) $game["id"] === $id) {
            $selectGame = $game;
            break;
        }
    }
    return $selectGame;
}

function getFavoriteList()
{
    global $games;
    $favoriteList = json_decode($_COOKIE["favoriteList"], true);

    $gamesList = [];
    foreach ($favoriteList as $favorite) {
        foreach ($games as $game) {
            if ($favorite == $game["id"]) {
                $gamesList[] = $game;
            }
        }
    }

    return $gamesList;
}

